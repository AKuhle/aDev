#ifdef _USE_QT_


/*******************************************************************************
* includes
*******************************************************************************/
#include "qPen.h"
#include "qRgba.h"



namespace qLib
{
    /*******************************************************************************
    * qPen::qPen
    *******************************************************************************/
    qPen::qPen(const qRgba &_rgba)
    : QPen(QColor::fromRgbF(_rgba.r, _rgba.g, _rgba.b, _rgba.a))
	{
    } // qPen::qPen


    /*******************************************************************************
    * qPen::~qPen
    *******************************************************************************/
    qPen::~qPen()
	{
    } // qPen::~qPen


    /*******************************************************************************
    * qPen::Color
    *******************************************************************************/
    qRgba qPen::Color() const
    {
        const QColor    &col = color();

        return qRgba(col.redF(), col.greenF(), col.blueF(), col.alphaF());
    } // qPen::Color

} // namespace qLib


#endif // _USE_QT_
