#ifdef _USE_QT_


/*******************************************************************************
* includes
*******************************************************************************/
#include "qBrush.h"
#include "qRgba.h"



namespace qLib
{
    /*******************************************************************************
    * qBrush::qBrush
    *******************************************************************************/
    qBrush::qBrush(const qRgba &_rgba)
    : QBrush(QColor::fromRgbF(_rgba.r, _rgba.g, _rgba.b, _rgba.a))
	{
    } // qBrush::qBrush


    /*******************************************************************************
    * qBrush::~qBrush
    *******************************************************************************/
    qBrush::~qBrush()
	{
    } // qBrush::~qBrush


    /*******************************************************************************
    * qBrush::Color
    *******************************************************************************/
    qRgba qBrush::Color() const
    {
        const QColor    &col = color();

        return qRgba(col.redF(), col.greenF(), col.blueF(), col.alphaF());
    } // qBrush::Color

} // namespace qLib


#endif // _USE_QT_
