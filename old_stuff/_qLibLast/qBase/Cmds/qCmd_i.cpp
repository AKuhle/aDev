/*******************************************************************************
* includes
*******************************************************************************/
#include "QCmd_I.h"



namespace qLib
{
    /*******************************************************************************
    * qCmdI::qCmdI
    *******************************************************************************/
    qCmdI::qCmdI(s32                _s32CmdId,
                 const qString16    &_sCmdName,
                 enumCmdType        _eCmdType,
                 qProgress          *_pProgress)
    : m_s32CmdId(_s32CmdId),
      m_sCmdName(_sCmdName),
      m_eCmdType(_eCmdType),
      m_pProgress(_pProgress)
    {
    } // qCmdI::qCmdI


    /*******************************************************************************
    * qCmdI::qCmdI
    *******************************************************************************/
    qCmdI::~qCmdI()
    {
    } // qCmdI::~qCmdI


    /*******************************************************************************
    * qCmdI::SetCmdMgr
    *******************************************************************************/
    void qCmdI::SetCmdMgr(qCmdMgr* _pCmdMgr)
    {
        m_pCmdMgr = _pCmdMgr;
    } // qCmdI::SetCmdMgr


    /*******************************************************************************
    * qCmdI::CmdMgr
    *******************************************************************************/
    qCmdMgr* qCmdI::CmdMgr() const
    {
        return m_pCmdMgr;
    } // qCmdI::CmdMgr


    /*******************************************************************************
    * qCmdI::Progress
    *******************************************************************************/
    qProgress* qCmdI::Progress() const
    {
        return m_pProgress;
    } // qCmdI::Progress


    /*******************************************************************************
    * qCmdI::CmdId
    *******************************************************************************/
    s32 qCmdI::CmdId() const
    {
        return m_s32CmdId;
    } // qCmdI::CmdId


    /*******************************************************************************
    * qCmdI::CmdName
    *******************************************************************************/
    const qString16& qCmdI::CmdName() const
    {
        return m_sCmdName;
    } // qCmdI::CmdName


    /*******************************************************************************
    * qCmdI::CmdType
    *******************************************************************************/
    enumCmdType qCmdI::CmdType() const
    {
        return m_eCmdType;
    } // qCmdI::CmdType


    /*******************************************************************************
    * qCmdI::CanUndo
    *******************************************************************************/
    bool qCmdI::CanUndo() const
    {
        return (m_eCmdType == enumCmdType::CMD_TYPE_UNDO);
    } // qCmdI::CanUndo

} // namespace qLib
