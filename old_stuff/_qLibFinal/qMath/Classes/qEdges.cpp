/*******************************************************************************
* includes
*******************************************************************************/
#include "qEdges.h"


namespace qLib
{
    /*******************************************************************************
    * qEdges::qEdges
    *******************************************************************************/
    qEdges::qEdges()
    {
    } // qEdges::qEdges


    /*******************************************************************************
    * qEdges::qEdges
    *******************************************************************************/
    qEdges::qEdges(s32 _lrtb)
    : l(_lrtb),
      r(_lrtb),
      t(_lrtb),
      b(_lrtb)
    {
    } // qEdges::qEdges


    /*******************************************************************************
    * qEdges::qEdges
    *******************************************************************************/
    qEdges::qEdges(s32  _lr,
                   s32  _tb)
    : l(_lr),
      r(_lr),
      t(_tb),
      b(_tb)
    {
    } // qEdges::qEdges


    /*******************************************************************************
    * qEdges::qEdges
    *******************************************************************************/
    qEdges::qEdges(s32  _l,
                   s32  _r,
                   s32  _t,
                   s32  _b)
    : l(_l),
      r(_r),
      t(_t),
      b(_b)
    {
    } // qEdges::qEdges


    /*******************************************************************************
    * qEdges::~qEdges
    *******************************************************************************/
    qEdges::~qEdges()
    {
    } // qEdges::~qEdges


    /*******************************************************************************
    * qEdges::l
    *******************************************************************************/
    void qEdges::Set(s32  _lrtb)
    {
        l = _lrtb;
        r = _lrtb;
        t = _lrtb;
        b = _lrtb;
    } // qEdges::l


    /*******************************************************************************
    * qEdges::l
    *******************************************************************************/
    void qEdges::Set(s32  _l,
                     s32  _r,
                     s32  _t,
                     s32  _b)
    {
        l = _l;
        r = _r;
        t = _t;
        b = _b;
    } // qEdges::l


    /*******************************************************************************
    * qEdges::w
    *******************************************************************************/
    s32 qEdges::w() const
    {
        return l + r;
    } // qEdges::w


    /*******************************************************************************
    * qEdges::h
    *******************************************************************************/
    s32 qEdges::h() const
    {
        return t + b;
    } // qEdges::h

} // namespace qLib
