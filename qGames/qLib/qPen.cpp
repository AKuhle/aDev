/*******************************************************************************
* includes
*******************************************************************************/
#include "qPen.h"
#include "qColor.h"



namespace qLib
{
    /*******************************************************************************
    * qPen::qPen
    *******************************************************************************/
    qPen::qPen(const qColor &_col)
    : QPen(QColor::fromRgbF(_col.r, _col.g, _col.b, _col.a))
	{
    } // qPen::qPen


    /*******************************************************************************
    * qPen::qPen
    *******************************************************************************/
    qPen::qPen(const qColor &_col,
               s32          _s32W)
    : QPen(QColor::fromRgbF(_col.r, _col.g, _col.b, _col.a))
    {
        setWidth(_s32W);
    } // qPen::qPen


    /*******************************************************************************
    * qPen::~qPen
    *******************************************************************************/
    qPen::~qPen()
	{
    } // qPen::~qPen


    /*******************************************************************************
    * qPen::GetColor
    *******************************************************************************/
    qColor qPen::GetColor() const
    {
        const QColor    &col = color();

        return qColor(col.redF(), col.greenF(), col.blueF(), col.alphaF());
    } // qPen::GetColor

} // namespace qLib
