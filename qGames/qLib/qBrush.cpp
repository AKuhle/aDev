/*******************************************************************************
* includes
*******************************************************************************/
#include "qBrush.h"
#include "qColor.h"



namespace qLib
{
    /*******************************************************************************
    * qBrush::qBrush
    *******************************************************************************/
    qBrush::qBrush(const qColor &_col)
    : QBrush(QColor::fromRgbF(_col.r, _col.g, _col.b, _col.a))
	{
    } // qBrush::qBrush


    /*******************************************************************************
    * qBrush::qBrush
    *******************************************************************************/
    qBrush::qBrush(Rgba_t   _r,
                   Rgba_t   _g,
                   Rgba_t   _b,
                   Rgba_t   _a /*= 1*/)
    : QBrush(QColor::fromRgbF(_r, _g, _b, _a))
    {
    } // qBrush::qBrush


    /*******************************************************************************
    * qBrush::~qBrush
    *******************************************************************************/
    qBrush::~qBrush()
	{
    } // qBrush::~qBrush


    /*******************************************************************************
    * qBrush::GetColor
    *******************************************************************************/
    qColor qBrush::GetColor() const
    {
        const QColor    &col = color();

        return qColor(col.redF(), col.greenF(), col.blueF(), col.alphaF());
    } // qBrush::GetColor

} // namespace qLib
