#ifdef _USE_QT_


/*******************************************************************************
* includes
*******************************************************************************/
#include "aPixmap.h"
#include "aString.h"

using namespace aFrame::aUtil;


/*******************************************************************************
* namespace
*******************************************************************************/
namespace aFrame {
namespace aGraphic {


/*******************************************************************************
* aPixmap::aPixmap
*******************************************************************************/
aPixmap::aPixmap()
: QPixmap()
{
} // aPixmap::aPixmap


/*******************************************************************************
* aPixmap::aPixmap
*******************************************************************************/
aPixmap::aPixmap(const QPixmap &_lhs)
: QPixmap(_lhs)
{
} // aPixmap::aPixmap


/*******************************************************************************
* aPixmap::aPixmap
*******************************************************************************/
aPixmap::aPixmap(const aString &_sFileName)
: QPixmap(_sFileName.toQString())
{
} // aPixmap::aPixmap


/*******************************************************************************
* aPixmap::~aPixmap
*******************************************************************************/
aPixmap::~aPixmap()
{
} // aPixmap:.~aPixmap


/*******************************************************************************
* aPixmap::isValid
*******************************************************************************/
bool aPixmap::isValid() const
{
    return !QPixmap::isNull();
} // aPixmap::isValid


/*******************************************************************************
* aPixmap::scale
*******************************************************************************/
void aPixmap::scale(s32    _s32W,
                    s32    _s32H)
{
    if (isValid())
    {
        *this = aPixmap(QPixmap::scaled(_s32W, _s32H));
    }
} // aPixmap::scale


} // namespace aGraphic
} // namespace aFrame

#endif // _USE_QT_
