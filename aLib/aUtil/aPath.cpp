/*******************************************************************************
* includes
*******************************************************************************/
#include "aPath.h"


/*******************************************************************************
* namespace
*******************************************************************************/
namespace aLib {
namespace aUtil {


/*******************************************************************************
* aPath::aPath
*******************************************************************************/
aPath::aPath()
{
} // aPath::aPath


/*******************************************************************************
* aPath::aPath
*******************************************************************************/
aPath::aPath(const aString  &_sPath)
: std::filesystem::path(_sPath.to_stdU16String())
{
} // aPath::aPath


/*******************************************************************************
* aPath::~aPath
*******************************************************************************/
aPath::~aPath()
{
} // aPath::~aPath


/*******************************************************************************
* aPath::operator=
*******************************************************************************/
aPath& aPath::operator=(const char *_pStr)
{
    *this += _pStr;

    return *this;
} // aPath::operator=


/*******************************************************************************
* aPath::operator=
*******************************************************************************/
aPath& aPath::operator=(const std::u16string &_str)
{
    std::filesystem::path::operator=(_str);

    return *this;
} // aPath::operator=


/*******************************************************************************
* aPath::operator+=
*******************************************************************************/
aPath& aPath::operator+=(const char *_pStr)
{
    std::filesystem::path::operator+=(_pStr);

    return *this;
} // aPath::operator+=


/*******************************************************************************
* aPath::operator+=
*******************************************************************************/
aPath& aPath::operator+=(const aString &_str)
{
    std::filesystem::path::operator +=(_str.to_stdU16String());

    return *this;
} // aPath::operator+=


/*******************************************************************************
* aPath::canonicalPath
*******************************************************************************/
aString aPath::canonicalPath() const
{
    //std::string npath = pp.make_preferred().string();

    return std::filesystem::weakly_canonical(*this).u16string();
} // aPath::canonicalPath


} // namespace aUtil
} // namespace aLib
