#pragma once


/*******************************************************************************
* includes
*******************************************************************************/
#include "aSysWin_sys.h"


/*******************************************************************************
* namespace
*******************************************************************************/
namespace aLib {
namespace aWin {


/*******************************************************************************
* class aSysWin
*******************************************************************************/
template<class T>
class aSysWin : public aSysWin_sys<T>
{
    /*******************************************************************************
    * con-/destruction
    *******************************************************************************/
    protected:
        aSysWin(SysWinClass *_pParent = nullptr);
        virtual ~aSysWin();

    public:
        bool                create();


    /*******************************************************************************
    * window state
    *******************************************************************************/
    public:
        // visibility
        bool                isVisible() const;
        void                setVisible(bool _bVisible);
        void                show();
        void                hide();


    /*******************************************************************************
    * handler
    *******************************************************************************/
    protected:
        virtual bool        onSysCreate();
        virtual bool        onCreate();

        virtual void        onDropUrl(const aUrl  &_url);


    /*******************************************************************************
    * system events
    *******************************************************************************/
    protected:
        virtual void        onOsDropUrl(const aUrl  &_url) override;

}; // class aSysWin


} // namespace aWin
} // namespace aLib


